int	ft_countwords(char const *s, char c)
{
	int i;
	int	n;

	i = 0;
	n = 0;
	if (!s)
		return (0);
	while (s[i] == c)
		i++;
	while (s[i])
	{
		if ((s[i] == c && s[i + 1] != c) || (s[i] != c && s[i + 1] == '\0'))
			n++;
		i++;
	}
	return (n);
}
